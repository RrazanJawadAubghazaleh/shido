package com.shidoapp.shido.pojo;

public class UsersInfo {

    String name;
    int image;
    int id;

    public UsersInfo() {
    }

    int numberOfMassiges;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getNumberOfMassiges() {
        return numberOfMassiges;
    }

    public void setNumberOfMassiges(int numberOfMassiges) {
        this.numberOfMassiges = numberOfMassiges;
    }
}
